"""
download_dataset.py

What this script does:
1. Tries to download the Fake-or-Real (FoR) dataset from Zenodo
2. If download fails, generates synthetic audio files using numpy
3. Organises everything into datasets/real/ and datasets/fake/ folders

Why two folders?
Our model needs to know which files are real and which are fake.
We use folder names as labels — real = 0, fake = 1.
"""

import os
import sys
import numpy as np
import requests
import zipfile
import soundfile as sf
from pathlib import Path
from tqdm import tqdm

# ── Configuration ─────────────────────────────────────────────────────────────
SAMPLE_RATE = 16000          # 16kHz — standard for speech
DURATION    = 3.0            # 3 seconds per clip
N_SAMPLES   = int(SAMPLE_RATE * DURATION)   # = 48000 samples per clip
N_SYNTHETIC = 400            # how many synthetic files to make per class
                             # if real download fails

# Where to save things
BASE_DIR    = Path(__file__).parent.parent   # the audio-deepfake-detector/ folder
DATASET_DIR = BASE_DIR / "datasets"
REAL_DIR    = DATASET_DIR / "real"
FAKE_DIR    = DATASET_DIR / "fake"

# ── Helper: create directories if they don't exist ────────────────────────────
def setup_dirs():
    REAL_DIR.mkdir(parents=True, exist_ok=True)
    FAKE_DIR.mkdir(parents=True, exist_ok=True)
    print(f"✓ Created: {REAL_DIR}")
    print(f"✓ Created: {FAKE_DIR}")

# ── Option A: Download FoR dataset from Zenodo ────────────────────────────────
def download_for_dataset():
    """
    The Fake-or-Real dataset is hosted on Zenodo (a research data platform).
    It contains real speech recordings and speech generated by TTS systems.
    
    We download a smaller subset to keep things fast.
    """
    print("\n── Attempting to download Fake-or-Real dataset ──")
    
    # This is a subset of the FoR dataset - for-norm folder
    url = "https://zenodo.org/record/4275027/files/for-norm.zip"
    zip_path = DATASET_DIR / "for-norm.zip"
    
    try:
        print(f"  Downloading from: {url}")
        print("  This may take a few minutes...")
        
        response = requests.get(url, stream=True, timeout=30)
        response.raise_for_status()   # raises error if download failed
        
        total_size = int(response.headers.get('content-length', 0))
        
        # Download with progress bar
        with open(zip_path, 'wb') as f, tqdm(
            total=total_size,
            unit='iB',
            unit_scale=True,
            desc="  Downloading"
        ) as bar:
            for chunk in response.iter_content(chunk_size=8192):
                size = f.write(chunk)
                bar.update(size)
        
        # Extract the zip file
        print("  Extracting files...")
        with zipfile.ZipFile(zip_path, 'r') as zf:
            zf.extractall(DATASET_DIR)
        
        # Move files into our real/ and fake/ folders
        organise_for_dataset()
        
        print("✓ FoR dataset downloaded and organised!")
        return True
        
    except Exception as e:
        print(f"  ✗ Download failed: {e}")
        print("  → Falling back to synthetic data generation...")
        return False

def organise_for_dataset():
    """
    The FoR zip extracts into folders like:
      for-norm/training/real/*.wav
      for-norm/training/fake/*.wav
    
    We copy a subset into our datasets/real/ and datasets/fake/ folders.
    """
    extracted = DATASET_DIR / "for-norm"
    
    count = 0
    for split in ["training", "validation", "testing"]:
        for label in ["real", "fake"]:
            src_dir = extracted / split / label
            dst_dir = REAL_DIR if label == "real" else FAKE_DIR
            
            if not src_dir.exists():
                continue
            
            wav_files = list(src_dir.glob("*.wav"))
            # Take up to 400 files per label to keep it manageable
            for wav_file in wav_files[:400]:
                dst_path = dst_dir / f"{label}_{split}_{wav_file.name}"
                if not dst_path.exists():
                    # Copy file
                    dst_path.write_bytes(wav_file.read_bytes())
                    count += 1
    
    print(f"  Organised {count} audio files")


# ── Option B: Generate synthetic audio ────────────────────────────────────────
def generate_real_audio(file_index: int) -> np.ndarray:
    """
    Generates audio that sounds like real speech — organic, noisy, irregular.
    
    Real speech has:
    - Multiple frequency components (harmonics) at irregular intervals
    - Natural amplitude variation (your voice gets louder and quieter)
    - Background noise
    - Random pitch variation (because nobody speaks perfectly flat)
    
    We simulate this with sine waves + randomness + noise.
    """
    
    # Random fundamental frequency (male voice: 85-180Hz, female: 165-255Hz)
    fundamental = np.random.uniform(100, 220)
    
    # Create harmonics (real voices have many harmonics, each getting quieter)
    # Harmonics are multiples of the fundamental frequency
    # Real voice: 100Hz, 200Hz, 300Hz... but with natural jitter
    audio = np.zeros(N_SAMPLES)
    t = np.linspace(0, DURATION, N_SAMPLES)  # time axis from 0 to 3 seconds
    
    for harmonic_num in range(1, 8):   # 7 harmonics
        freq = fundamental * harmonic_num
        
        # Each harmonic is quieter than the last (realistic)
        amplitude = 1.0 / harmonic_num
        
        # Real speech has slight pitch variation over time (vibrato-like)
        # We add a slow wobble to the frequency
        freq_wobble = freq + np.random.uniform(-2, 2) * np.sin(2 * np.pi * 0.5 * t)
        
        # Slowly changing phase makes it sound organic
        phase = np.random.uniform(0, 2 * np.pi)
        
        audio += amplitude * np.sin(2 * np.pi * freq_wobble * t + phase)
    
    # Add natural amplitude envelope — speech has pauses and emphasis
    # This creates a "vowel-consonant-vowel" like pattern
    envelope = np.ones(N_SAMPLES)
    for _ in range(np.random.randint(3, 7)):
        # Random dips in the envelope (like brief pauses between syllables)
        dip_start = np.random.randint(0, N_SAMPLES - 3000)
        dip_len   = np.random.randint(500, 3000)
        envelope[dip_start:dip_start + dip_len] *= np.random.uniform(0.1, 0.4)
    
    audio *= envelope
    
    # Add pink-ish noise (real recordings always have background noise)
    noise = np.random.randn(N_SAMPLES) * 0.03
    audio += noise
    
    # Normalise to [-1, 1] range
    max_val = np.abs(audio).max()
    if max_val > 0:
        audio = audio / max_val * 0.9
    
    return audio.astype(np.float32)


def generate_fake_audio(file_index: int) -> np.ndarray:
    """
    Generates audio that looks like AI-generated speech in a spectrogram.
    
    AI voices have:
    - Too-perfect harmonic spacing (no natural wobble)
    - Sudden sharp changes at frame boundaries (GAN artifact)
    - Very clean high frequencies with abrupt cutoff
    - Unnaturally consistent amplitude (no real envelope variation)
    
    We simulate these artifacts.
    """
    
    fundamental = np.random.uniform(100, 220)
    
    audio = np.zeros(N_SAMPLES)
    t = np.linspace(0, DURATION, N_SAMPLES)
    
    # GAN harmonics: too perfect, no wobble, too regular
    for harmonic_num in range(1, 6):
        freq = fundamental * harmonic_num   # perfect integer multiples, no jitter
        amplitude = 1.0 / harmonic_num
        # No frequency wobble — robotic flat pitch
        audio += amplitude * np.sin(2 * np.pi * freq * t)
    
    # Frame boundary artifacts: GAN generates audio in chunks of ~256 samples
    # At each chunk boundary, there's a slight discontinuity
    FRAME_SIZE = 256
    for frame_start in range(0, N_SAMPLES, FRAME_SIZE):
        frame_end = min(frame_start + FRAME_SIZE, N_SAMPLES)
        # Add a tiny click/bump at each frame boundary
        if frame_start > 0:
            audio[frame_start:frame_start+4] += np.random.uniform(-0.05, 0.05)
    
    # Unnaturally flat amplitude — AI doesn't model prosody well
    # Real speech varies; AI is boringly consistent
    flat_envelope = np.ones(N_SAMPLES) * np.random.uniform(0.7, 0.9)
    audio *= flat_envelope
    
    # Sharp frequency cutoff: GAN models drop off sharply above ~4kHz
    # We simulate this by removing high-frequency content from the noise
    # (low-pass filter effect — the noise is too "clean")
    noise = np.random.randn(N_SAMPLES) * 0.008   # much less noise than real
    audio += noise
    
    # Slight quantization artifacts (digital clipping effect)
    # Some GAN models over-compress the output
    audio = np.clip(audio, -0.85, 0.85)
    
    # Normalise
    max_val = np.abs(audio).max()
    if max_val > 0:
        audio = audio / max_val * 0.9
    
    return audio.astype(np.float32)


def generate_synthetic_dataset():
    """
    Generates N_SYNTHETIC real clips + N_SYNTHETIC fake clips
    and saves them as .wav files.
    """
    print(f"\n── Generating synthetic dataset ({N_SYNTHETIC} real + {N_SYNTHETIC} fake) ──")
    
    # Generate REAL audio files
    print(f"  Generating {N_SYNTHETIC} real audio clips...")
    for i in tqdm(range(N_SYNTHETIC), desc="  Real audio"):
        audio = generate_real_audio(i)
        save_path = REAL_DIR / f"real_{i:04d}.wav"
        sf.write(save_path, audio, SAMPLE_RATE)
    
    # Generate FAKE audio files
    print(f"  Generating {N_SYNTHETIC} fake audio clips...")
    for i in tqdm(range(N_SYNTHETIC), desc="  Fake audio"):
        audio = generate_fake_audio(i)
        save_path = FAKE_DIR / f"fake_{i:04d}.wav"
        sf.write(save_path, audio, SAMPLE_RATE)
    
    print(f"✓ Synthetic dataset generated!")
    print(f"  Real files: {len(list(REAL_DIR.glob('*.wav')))}")
    print(f"  Fake files: {len(list(FAKE_DIR.glob('*.wav')))}")


# ── Main entry point ──────────────────────────────────────────────────────────
if __name__ == "__main__":
    print("=" * 55)
    print("  Audio Deepfake Detector — Dataset Downloader")
    print("=" * 55)
    
    setup_dirs()
    
    # Check if we already have data
    existing_real = len(list(REAL_DIR.glob("*.wav")))
    existing_fake = len(list(FAKE_DIR.glob("*.wav")))
    
    if existing_real >= 100 and existing_fake >= 100:
        print(f"\n✓ Dataset already exists:")
        print(f"  Real: {existing_real} files")
        print(f"  Fake: {existing_fake} files")
        print("  Skipping download. Delete datasets/ to re-download.")
        sys.exit(0)
    
    # Try to download real dataset first
    success = download_for_dataset()
    
    # If download failed or didn't give us enough files, generate synthetic
    real_count = len(list(REAL_DIR.glob("*.wav")))
    fake_count = len(list(FAKE_DIR.glob("*.wav")))
    
    if not success or real_count < 100 or fake_count < 100:
        generate_synthetic_dataset()
    
    # Final count
    real_count = len(list(REAL_DIR.glob("*.wav")))
    fake_count = len(list(FAKE_DIR.glob("*.wav")))
    
    print("\n" + "=" * 55)
    print("  Dataset Ready!")
    print(f"  Real files: {real_count}")
    print(f"  Fake files: {fake_count}")
    print("=" * 55)